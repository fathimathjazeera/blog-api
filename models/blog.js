import mongoose from "mongoose";

const BolgSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  user: {
    type: mongoose.Types.ObjectId,
    ref: "User",
    required: true,
  },
});

const BlogModel = mongoose.model("Blog", BolgSchema);
export default BlogModel;
